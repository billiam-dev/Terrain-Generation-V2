        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        readonly int3 FaceToCellIndex(int x, int y, int z)
        {
            int scaledChunkSize = chunkSize * 2;

            return transitionIndex switch
            {
                0 => new(scaledChunkSize - z, y, x), //  x
                1 => new(z, x, y),                   // -x
                2 => new(x, scaledChunkSize - z, y), //  y
                3 => new(y, z, x),                   // -y
                4 => new(y, x, scaledChunkSize - z), //  z
                5 => new(x, y, z),                   // -z
                _ => new(0, 0, 0)
            };
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        readonly float3 ComputeNormal(int3 coord)
        {
            int3[] axis = TransvoxelTables.TransitionAxis[transitionIndex];

            float3 normal;
            normal.x = SampleDensity(coord - axis[0]) - SampleDensity(coord + axis[0]);
            normal.y = SampleDensity(coord - axis[1]) - SampleDensity(coord + axis[1]);
            normal.z = SampleDensity(coord - axis[2]) - SampleDensity(coord + axis[2]);
            return normal;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        unsafe readonly float SampleDensity(int3 cellIndex)
        {
            // z-axis is indexed at half the resolution of the x and y axis.
            cellIndex.z /= 2;

            // Re-align cell index with density map by negating the offsets used in the density evaluation step.
            cellIndex.x += 2;
            cellIndex.y += 2;
            cellIndex.z += 1;

            int index = MeshingHelpers.FlattenIndex(cellIndex, pointsPerAxis);
            float* ptr = (float*)densityPtr;

            return *(ptr + index);
        }
    }

    public static class MeshingHelpers
    {
        /// <summary>
        /// How much of a full cell is shifted to make room for transition cells.
        /// </summary>
        const float k_TransitionCellPadding = 0.2f;

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static int GetEdgeMask(int3 i0, int3 i1, int min, int max)
        {
            return (i0.x == max || i1.x == max ? 1  : 0)
                 | (i0.x == min || i1.x == min ? 2  : 0)
                 | (i0.y == max || i1.y == max ? 4  : 0)
                 | (i0.y == min || i1.y == min ? 8  : 0)
                 | (i0.z == max || i1.z == max ? 16 : 0)
                 | (i0.z == min || i1.z == min ? 32 : 0);
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static float3 GetTransitionDelta(int edgeMask, float3 position, int chunkSize)
        {
            float3 delta = 0;

            float farDelta = chunkSize - 1.0f;
            float closeDelta = 1.0f;

            if ((edgeMask & 1) == 1 && position.x >= farDelta)
            {
                delta.x = farDelta - position.x;
            }
            else if ((edgeMask & 2) == 2 && position.x <= closeDelta)
            {
                delta.x = closeDelta - position.x;
            }

            if ((edgeMask & 4) == 4 && position.y >= farDelta)
            {
                delta.y = farDelta - position.y;
            }
            else if ((edgeMask & 8) == 8 && position.y <= closeDelta)
            {
                delta.y = closeDelta - position.y;
            }

            if ((edgeMask & 16) == 16 && position.z >= farDelta)
            {
                delta.z = farDelta - position.z;
            }
            else if ((edgeMask & 32) == 32 && position.z <= closeDelta)
            {
                delta.z = closeDelta - position.z;
            }

            return delta * k_TransitionCellPadding;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static float3 ReprojectPointWithNormal(float3 p, float3 o, float3 n)
        {
            // Reproject position with offset along interpolated normal vector (p47, Equation 4.3).

            float a = -n.x * n.y;
            float b = -n.z * n.x;
            float c = -n.y * n.z;

            float3 n2 = 1.0f - math.square(n);

            float3 newPoint;
            newPoint.x = p.x + (n2.x * o.x) + (a * o.y) + (b * o.z);
            newPoint.y = p.y + (a * o.x) + (n2.y * o.y) + (c * o.z);
            newPoint.z = p.z + (b * o.x) + (c * o.y) + (n2.z * o.z);

            return newPoint;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static float3 TransformPosition(float3 position, int chunkSize, int levelScale, float worldScale)
        {
            return levelScale * worldScale * (position - (chunkSize / 2));
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static int FlattenIndex(int3 index, int size)
        {
            return (index.z * size * size) + (index.y * size) + index.x;
        }
    }

