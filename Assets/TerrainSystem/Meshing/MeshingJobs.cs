    [BurstCompile(OptimizeFor = OptimizeFor.Performance, FloatMode = FloatMode.Fast, FloatPrecision = FloatPrecision.Low, CompileSynchronously = true, DisableSafetyChecks = true)]
    public struct CoreMeshingJob : IJob
    {
        // Input terrain data
        [ReadOnly] public int3 chunkIndex;            // Index into the density brick map at this clipmap level.
        [ReadOnly] public int chunkSize;              // Number of cells per axis in a density chunk.
        [ReadOnly] public int levelScale;             // The scale of the underlying density data, based on the brickmap level.
        [ReadOnly] public float worldScale;           // World scale of a single cell, determines the scale of the entire terrain.

        [ReadOnly, NativeDisableUnsafePtrRestriction]
        public IntPtr densityPtr;                     // Pointer to underlying density data.

        [ReadOnly] public int pointsPerAxis;          // The number of data points per axis in the density array.

        // Output mesh data
        [NativeDisableParallelForRestriction]
        public NativeList<Vertex> vertices;

        [NativeDisableParallelForRestriction]
        public NativeList<ushort> indices;

        // Reusable cells array
        [NativeDisableParallelForRestriction]
        public NativeArray<ReuseCellVertexIndices> vertexIndices;

        public void Execute()
        {
            // March all cells to create default meshes.
            for (int x = 0; x < chunkSize; x++)
                for (int y = 0; y < chunkSize; y++)
                    for (int z = 0; z < chunkSize; z++)
                        MarchCell(new int3(x, y, z));
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        void MarchCell(int3 index)
        {
            // Sample the density at the 8 corner indices of the cell.
            CubeCorners<float> density = new();
            for (int i = 0; i < 8; i++)
                density[i] = SampleDensity(index + TransvoxelTables.CornerOffsets[i]);

            // Find the unique 'cube configuration' for this cell, using the density values at each corner point.
            // The cube configuration is used as a lookup in the trinangulation table, which tells the algorithm which vertices to connect for this specific set of density values.

            // We pack the 8 density values as float4's to utilize SIMD, see: https://eetumaenpaa.fi/blog/marching-cubes-optimizations-in-unity/#voxelcorners-vs-stackalloc
            float4 packedDensity1 = new(density[0], density[1], density[2], density[3]);
            float4 packedDensity2 = new(density[4], density[5], density[6], density[7]);

            // Math.select should always be used in place of a branch in unity JOBS.
            int4 p1 = math.select(0, new int4(1, 2, 4, 8), packedDensity1 < 0);
            int4 p2 = math.select(0, new int4(16, 32, 64, 128), packedDensity2 < 0);

            byte caseCode = (byte)(math.csum(p1) | math.csum(p2));

            // Exit early if there are no intersections, i.e. the cell is entirely full or empty.
            if (caseCode == 0 || caseCode == 255)
                return;

            ref byte cellClass = ref TransvoxelTables.RegularCellClass[caseCode];
            ref byte cellData = ref TransvoxelTables.RegularCellData[cellClass][0];

            // First element of cellData is the geometry counts data, contains both vertex and triangle count data.
            // The remaining elements of the cellData are the vertex indices; which vertices need to be connected to form the correct face.
            int vertexCount = cellData >> 4;
            int triangleCount = cellData & 0x0F;

            // Construct indices for this cell, trying to reuse as many vertices as possible. These are stored in the CellIndices object.
            CellIndices<ushort> cellIndices = new();
            ReuseCellVertexIndices reuseCell = new();

            ushort[] edgeCodes = TransvoxelTables.RegularVertexData[caseCode];
            for (int i = 0; i < vertexCount; i++)
            {
                ushort edgeCode = edgeCodes[i];

                byte cornerIdx0 = (byte)((edgeCode >> 4) & 0x0F);
                byte cornerIdx1 = (byte)(edgeCode & 0x0F);

                byte cellDir = (byte)(edgeCode >> 12);
                byte vertexEdgeIndex = (byte)((edgeCode >> 8) & 0x0F);

                // The cases presented to handle duplicate vertices at t values of 1 or 0 are so rare in actual terrain, so we don't bother to account for them here (hence why ReuseCell does not handle the 0 case, see p23, Figure 3.9).
                // This leaves vertex reusing at the three maximal edges of the cell, which makes a signficant difference.
                
                // Check if we are at the maximum edges of a cell. If so, always produce a new vertex.
                if (cellDir == 8)
                {
                    ushort vertexIndex = CreateVertex(ref density, index, cornerIdx0, cornerIdx1);
                    cellIndices[i] = vertexIndex;

                    reuseCell[vertexEdgeIndex] = vertexIndex;
                }
                else
                {
                    // The direction to travel to find a preceding cell with a reusable vertex can be derrived from the edge code...
                    int3 reuseCellOffset;
                    reuseCellOffset.z = -((cellDir >> 2) & 1);
                    reuseCellOffset.y = -((cellDir >> 1) & 1);
                    reuseCellOffset.x = -(cellDir & 1);

                    // If we are the minimum edge of a chunk and trying to reuse a cell that is out of bounds, always create a new vertex.
                    // Else, always reuse a vertex from a preceding cell.
                    if ((index.x == 0 && reuseCellOffset.x == -1) ||
                        (index.y == 0 && reuseCellOffset.y == -1) ||
                        (index.z == 0 && reuseCellOffset.z == -1))
                    {
                        cellIndices[i] = CreateVertex(ref density, index, cornerIdx0, cornerIdx1);
                    }
                    else
                    {
                        cellIndices[i] = vertexIndices[MeshingHelpers.FlattenIndex(index + reuseCellOffset, chunkSize)][vertexEdgeIndex];
                    }
                }
            }

            // Store the cell for reuse.
            vertexIndices[MeshingHelpers.FlattenIndex(index, chunkSize)] = reuseCell;

            // Form triangles from the indices above.
            for (int i = 0; i < triangleCount; i++)
            {
                indices.Add(cellIndices[TransvoxelTables.RegularCellData[cellClass][i * 3 + 1]]);
                indices.Add(cellIndices[TransvoxelTables.RegularCellData[cellClass][i * 3 + 2]]);
                indices.Add(cellIndices[TransvoxelTables.RegularCellData[cellClass][i * 3 + 3]]);
            }
        }

        ushort CreateVertex(ref CubeCorners<float> density, int3 index, byte cornerIdx0, byte cornerIdx1)
        {
            // Sample density at cell corners.
            float d0 = density[cornerIdx0];
            float d1 = density[cornerIdx1];

            // Calculate interpolator.
            float t = d0 / (d0 - d1);

            // Get edge vertex positions.
            int3 cornerCoord0 = index + TransvoxelTables.CornerOffsets[cornerIdx0];
            int3 cornerCoord1 = index + TransvoxelTables.CornerOffsets[cornerIdx1];

            // Make edge mask.
            int edgeMask = levelScale > 1 ? MeshingHelpers.GetEdgeMask(cornerCoord0, cornerCoord1, 0, chunkSize) : 0;

            // Compute normal.
            float3 normal = -math.normalize(math.lerp(ComputeNormal(cornerCoord0), ComputeNormal(cornerCoord1), t));

            // Compute primary position.
            float3 primaryPos = math.lerp((float3)cornerCoord0, (float3)cornerCoord1, t);

            // Compute secondary position.
            float3 secondaryPos = primaryPos;
            if (edgeMask > 0)
                secondaryPos = MeshingHelpers.ReprojectPointWithNormal(primaryPos, MeshingHelpers.GetTransitionDelta(edgeMask, primaryPos, chunkSize), normal);

            // Add vertex and return its index.
            vertices.Add(new Vertex(
                MeshingHelpers.TransformPosition(primaryPos, chunkSize, levelScale, worldScale),
                MeshingHelpers.TransformPosition(secondaryPos, chunkSize, levelScale, worldScale),
                normal,
                (ushort)edgeMask));

            return (ushort)(vertices.Length - 1);
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        readonly float3 ComputeNormal(int3 coord)
        {
            float3 normal;
            normal.x = SampleDensity(coord - TransvoxelTables.Axis[0]) - SampleDensity(coord + TransvoxelTables.Axis[0]);
            normal.y = SampleDensity(coord - TransvoxelTables.Axis[1]) - SampleDensity(coord + TransvoxelTables.Axis[1]);
            normal.z = SampleDensity(coord - TransvoxelTables.Axis[2]) - SampleDensity(coord + TransvoxelTables.Axis[2]);
            return normal;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        unsafe readonly float SampleDensity(int3 cellIndex)
        {
            int index = MeshingHelpers.FlattenIndex(cellIndex + 1, pointsPerAxis);
            float* ptr = (float*)densityPtr;

            return *(ptr + index);
        }
    }

    [BurstCompile(OptimizeFor = OptimizeFor.Performance, FloatMode = FloatMode.Fast, FloatPrecision = FloatPrecision.Low, CompileSynchronously = true, DisableSafetyChecks = true)]
    public struct TransitionMeshingJob : IJob
    {
        // Input terrain data
        [ReadOnly] public int3 chunkIndex;            // Index into the density brick map at this clipmap level.
        [ReadOnly] public int chunkSize;              // Number of cells per axis in a density chunk.
        [ReadOnly] public int levelScale;             // The scale of the underlying density data, based on the brickmap level.
        [ReadOnly] public float worldScale;           // World scale of a single cell, determines the scale of the entire terrain.
        [ReadOnly] public int transitionIndex;        // The index of this transition JOB, 0 -> 5 = x, -x, y, -y, z, -z.

        [ReadOnly, NativeDisableUnsafePtrRestriction]
        public IntPtr densityPtr;                     // Pointer to underlying density data.

        [ReadOnly] public int pointsPerAxis;          // The number of data points per axis in the density array.

        // Output mesh data
        [NativeDisableParallelForRestriction]
        public NativeList<Vertex> vertices;

        [NativeDisableParallelForRestriction]
        public NativeList<ushort> indices;

        public void Execute()
        {
            for (int x = 0; x < chunkSize; x++)
                for (int y = 0; y < chunkSize; y++)
                    MarchCell(new int3(x, y, 0));
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        void MarchCell(int3 index)
        {
            // Sample density at 9 transition points on intersecting chunk edges. This alone determines the case code.
            TransitionCorners<float> density = new();
            for (int i = 0; i < 9; i++)
                density[i] = SampleDensity((index * 2) + TransvoxelTables.TransitionCornerOffsets[i]);

            float3 packedDensity1 = new(density[0], density[1], density[2]);
            float3 packedDensity2 = new(density[5], density[8], density[7]);
            float3 packedDensity3 = new(density[6], density[3], density[4]);

            int3 p1 = math.select(0, new int3(1, 2, 4), packedDensity1 < 0);
            int3 p2 = math.select(0, new int3(8, 16, 32), packedDensity2 < 0);
            int3 p3 = math.select(0, new int3(64, 128, 256), packedDensity3 < 0);

            // The max value of a byte (8 bits) is 255, I use a ushort (16 bits) because there are twice as many legal case codes for transitions.
            ushort caseCode = (ushort)(math.csum(p1) | math.csum(p2) | math.csum(p3));

            if (caseCode == 0 || caseCode == 511)
                return;

            ref byte cellClass = ref TransvoxelTables.TransitionCellClass[caseCode];
            byte cellDataIdx = (byte)(cellClass & 0x7F);
            ref byte cellData = ref TransvoxelTables.TransitionCellData[cellDataIdx][0];

            int vertexCount = cellData >> 4;
            int triangleCount = cellData & 0x0F;

            CellIndices<ushort> cellIndices = new();

            ushort[] edgeCodes = TransvoxelTables.TransitionVertexData[caseCode];
            for (int i = 0; i < vertexCount; i++)
            {
                ushort edgeCode = edgeCodes[i];

                byte cornerIdx0 = (byte)((edgeCode >> 4) & 0x0F);
                byte cornerIdx1 = (byte)(edgeCode & 0x0F);

                cellIndices[i] = CreateVertex(ref density, index, cornerIdx0, cornerIdx1);

                // TODO: Reuse vertices in transition cells.
                /*
                byte cellDirection = (byte)(edgeCode >> 12);
                int2 cellOffset = TransitionCellOffsetFromEdgeCode(cellDirection, transitionIndex);

                int vertexEdgeIndex = (edgeCode >> 8) & 0x0F;

                if (cellDirection == 8 || cellDirection == 4)
                {
                    cellIndices[i] = AddTransitionVertex(ref density, coreIndex, cornerIdx0, cornerIdx1, transitionIndex);
                }
                else
                {

                }
                */
            }

            bool reverseWinding = (cellClass & 0x80) > 0;

            if (reverseWinding)
            {
                for (int i = 0; i < triangleCount; i++)
                {
                    indices.Add(cellIndices[TransvoxelTables.TransitionCellData[cellDataIdx][i * 3 + 3]]);
                    indices.Add(cellIndices[TransvoxelTables.TransitionCellData[cellDataIdx][i * 3 + 2]]);
                    indices.Add(cellIndices[TransvoxelTables.TransitionCellData[cellDataIdx][i * 3 + 1]]);
                }
            }
            else
            {
                for (int i = 0; i < triangleCount; i++)
                {
                    indices.Add(cellIndices[TransvoxelTables.TransitionCellData[cellDataIdx][i * 3 + 1]]);
                    indices.Add(cellIndices[TransvoxelTables.TransitionCellData[cellDataIdx][i * 3 + 2]]);
                    indices.Add(cellIndices[TransvoxelTables.TransitionCellData[cellDataIdx][i * 3 + 3]]);
                }
            }
        }

        ushort CreateVertex(ref TransitionCorners<float> density, int3 index, byte cornerIdx0, byte cornerIdx1)
        {
            // Sample density at cell corners.
            float d0 = density[cornerIdx0];
            float d1 = density[cornerIdx1];

            // Calculate interpolator.
            float t = d0 / (d0 - d1);

            // Get edge vertex positions.
            int3 faceCorner0 = (index * 2) + TransvoxelTables.TransitionCornerOffsets[cornerIdx0];
            int3 faceCorner1 = (index * 2) + TransvoxelTables.TransitionCornerOffsets[cornerIdx1];

            // Flatten the cell against the chunk edge.
            // (It will be un-flattended by the secondary vertex positions).
            faceCorner0.z = 0;
            faceCorner1.z = 0;

            // Convert indices from face-space to chunk-space.
            int3 cornerCoord0 = FaceToCellIndex(faceCorner0.x, faceCorner0.y, faceCorner0.z);
            int3 cornerCoord1 = FaceToCellIndex(faceCorner1.x, faceCorner1.y, faceCorner1.z);

            // Make edge mask.
            int edgeMask = 0;
            if (cornerIdx0 > 8 && cornerIdx1 > 8)
                edgeMask = MeshingHelpers.GetEdgeMask(cornerCoord0 / 2, cornerCoord1 / 2, 0, chunkSize);

            // Compute normal.
            float3 normal = -math.normalize(math.lerp(ComputeNormal(faceCorner0), ComputeNormal(faceCorner1), t));

            // Compute primary position.
            float3 primaryPos = math.lerp((float3)cornerCoord0 * 0.5f, (float3)cornerCoord1 * 0.5f, t);

            // Compute secondary position.
            float3 secondaryPos = primaryPos;
            if (edgeMask > 0)
                secondaryPos = MeshingHelpers.ReprojectPointWithNormal(primaryPos, MeshingHelpers.GetTransitionDelta(edgeMask, primaryPos, chunkSize), normal);

            // Add vertex and return its index.
            vertices.Add(new Vertex(
                MeshingHelpers.TransformPosition(primaryPos, chunkSize, levelScale, worldScale),
                MeshingHelpers.TransformPosition(secondaryPos, chunkSize, levelScale, worldScale),
                normal,
                (ushort)edgeMask));

            return (ushort)(vertices.Length - 1);
        }

