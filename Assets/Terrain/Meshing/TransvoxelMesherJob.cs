    readonly struct TransvoxelTables
    {
        public static readonly int3[] CornerOffsets =
        {
            new(0, 0, 0),
            new(1, 0, 0),
            new(0, 1, 0),
            new(1, 1, 0),
            new(0, 0, 1),
            new(1, 0, 1),
            new(0, 1, 1),
            new(1, 1, 1)
        };

        // The RegularCellClass table maps an 8-bit regular Marching Cubes case index to an equivalence class index.
        // Even though there are 18 equivalence classes in our modified Marching Cubes algorithm,
        // a couple of them use the same exact triangulations, just with different vertex locations.
        // We combined those classes for this table so that the class index ranges from 0 to 15.
        public static readonly byte[] RegularCellClass =
        {
            0x00, 0x01, 0x01, 0x03, 0x01, 0x03, 0x02, 0x04, 0x01, 0x02, 0x03, 0x04, 0x03, 0x04, 0x04, 0x03,
